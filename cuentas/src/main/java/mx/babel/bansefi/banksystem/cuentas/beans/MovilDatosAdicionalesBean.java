package mx.babel.bansefi.banksystem.cuentas.beans;

public class MovilDatosAdicionalesBean {


	private String fecha;
	private String cuenta;
	private String num_telefono;
	private String nombre;
	private String num_telefono_new;
	private String documento;
	/**
	 * @return the documento
	 */
	public String getDocumento() {
		return documento;
	}
	/**
	 * @param documento the documento to set
	 */
	public void setDocumento(String documento) {
		this.documento = documento;
	}
	/**
	 * @return the fecha
	 */
	public String getFecha() {
		return fecha;
	}
	/**
	 * @param fecha the fecha to set
	 */
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	/**
	 * @return the cuenta
	 */
	public String getCuenta() {
		return cuenta;
	}
	/**
	 * @param cuenta the cuenta to set
	 */
	public void setCuenta(String cuenta) {
		this.cuenta = cuenta;
	}
	/**
	 * @return the num_telefono
	 */
	public String getNum_telefono() {
		return num_telefono;
	}
	/**
	 * @param num_telefono the num_telefono to set
	 */
	public void setNum_telefono(String num_telefono) {
		this.num_telefono = num_telefono;
	}
	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}
	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	/**
	 * @return the num_telefono_new
	 */
	public String getNum_telefono_new() {
		return num_telefono_new;
	}
	/**
	 * @param num_telefono_new the num_telefono_new to set
	 */
	public void setNum_telefono_new(String num_telefono_new) {
		this.num_telefono_new = num_telefono_new;
	}

}
